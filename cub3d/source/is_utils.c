#include "../include/cub3d.h"

bool	is_floor(char c)
{
	return (c == '0');
}

bool	is_wall(char c)
{
	return (c == '1');
}

int	is_space(char c)
{
	return (c == 32 || (c >= 9 && c <= 13));
}
